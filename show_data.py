def print_pokemon_data(pokemon):
    print(pokemon.name.upper() + " POKEDEX DATA:")
    print("HP: " + pokemon.hp +
          "\nAttack: " + pokemon.attack +
          "\nDefense: " + pokemon.defense +
          "\nSP Attack: " + pokemon.sp_attack +
          "\nSP Defense: " + pokemon.sp_defense +
          "\nSpeed: " + pokemon.speed)
